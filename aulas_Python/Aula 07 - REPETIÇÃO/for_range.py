lista = ["Naruto", "Sasuke", "Sakura", "Neji", "Kakashi", "Itachi"]

# 'range()' cria uma sequência de números. Seu formato padrão é 'range(start, stop, step)'.

# 'start' é o número inicial da sequência.
# 'stop' é o número final da sequência (o último número gerado será 'stop -1').
# 'step' é o intervalo entre os números na sequência.

for i in range(0, 5, 1):
    print(lista[i])